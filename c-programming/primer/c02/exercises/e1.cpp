#include <cstdio>

int main() {
    // Use one printf() call to print your first name and last name
    //  on one line
    printf("Joshua Tate\n");
    // Uses a second printf() call to print your first and last names
    //  on two separate lines
    printf("Joshua\n");
    printf("Tate\n");
    // Uses a pair of printf() calls to print your first and last names
    //  on one line.
    printf("Joshua ");
    printf("Tate\n");

    return 0;
}
